public abstract class Buildings
{
    /** Attributes */
    public String name;
    public String buildingPicturePath;
    public People people;

    /**
     *
     * @return String
     */
    public String getName() {
        return name;
    }

    /**
     *
     * @return String
     */
    public String getBuildingPicturePath() {
        return buildingPicturePath;
    }

}

